#string concatenation


#string can be concatenated using + operator and repeated using * operator

print('abhishek'+'joshi')
print(3*'hello')

#two or more string literals enclosed in quotes can be automatically concatenated
#without + sign.

print('abhishek'' joshi')

#above structure does not work with variable and literals
abc='python'
#print('i love'abc) does not get executed in shell also
#but can work with + sign

print('i love '+abc)
