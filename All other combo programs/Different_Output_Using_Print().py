#Syntax: print(value(s), sep= ‘ ‘, end = ‘\n’)
#default seperator is ' ' i.e. space
#default end is with '\n'

#printing without seperator
print('a','b','h','i')

#printing with seperator
print('a','b','h','i',sep='')#no spaces

#printing without end statement
print('python')
print('tutorials')

#printing with end statement
print('python',end='@')
print('tutorials')
